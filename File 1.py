def add(a, b):
    """
    Perform addition of two numbers.
    
    Args:
        a: First number
        b: Second number
        c: Third number
    
    Returns:
        Sum of a, b, and c
    """
    return a + b + c



if __name__ == "__main __":
    result = add(5, 3, 4)
    print(f"5 + 3 + 4 = {result}")